import math

def find_sqrt(num):
    return math.sqrt(num)

def find_ceil(num):
    return math.ceil(num)

def adding(number1, number2):
    return number1+number2

def subtracting(number1, number2):
    return number1-number2

def multiply(number1, number2):
    return number1*number2

def divide(number1, number2):
    return number1/number2

